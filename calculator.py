# Take user input and convert to integers right away
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Addition
sum_of_num = num1 + num2
print("Sum of two numbers:", sum_of_num)

# Subtraction
diff_of_num = num1 - num2
print("Difference of two numbers:", diff_of_num)

# Multiplication
prod_of_num = num1 * num2
print("Product of two numbers:", prod_of_num)

# Division
div_of_num = num1 / num2
print("Division of two numbers:", div_of_num)

